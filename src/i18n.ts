export function resolveAppLanguage(language: AppLanguage): ResolvedLanguage {
  if (language !== 'system') return language
  if (typeof navigator === 'undefined') return 'zh-CN'
  const languages = navigator.languages?.length ? navigator.languages : [navigator.language]
  return languages.some((item) => item.toLowerCase().startsWith('zh')) ? 'zh-CN' : 'en-US'
}

export function getSystemTimeZone() {
  try {
    return Intl.DateTimeFormat().resolvedOptions().timeZone || 'UTC'
  } catch {
    return 'UTC'
  }
}

export function getTimeZoneOffsetLabel(timeZone: string, language: ResolvedLanguage) {
  try {
    const locale = language === 'zh-CN' ? 'zh-CN' : 'en-US'
    const part = new Intl.DateTimeFormat(locale, {
      timeZone,
      timeZoneName: 'longOffset',
    }).formatToParts(new Date()).find((item) => item.type === 'timeZoneName')?.value
    return (part || 'UTC').replace('GMT', 'UTC')
  } catch {
    return 'UTC'
  }
}

export function translateUiText(text: string, language: ResolvedLanguage): string {
  if (language === 'zh-CN') return text
  const direct = englishUi[text]
  if (direct) return direct

  const workspace = text.match(/^W(\d+) 工作台$/)
  if (workspace) return `W${workspace[1]} Workspace`
  const numberedWidget = text.match(/^(本地终端|文件管理|机器占用)\s*(\d+)$/)
  if (numberedWidget) return `${englishUi[numberedWidget[1]] ?? numberedWidget[1]} ${numberedWidget[2]}`
  const remoteFile = text.match(/^(.+) 文件管理$/)
  if (remoteFile) return `${remoteFile[1]} File manager`
  const remoteMonitor = text.match(/^(.+) 机器监控$/)
  if (remoteMonitor) return `${remoteMonitor[1]} Monitor`
  const added = text.match(/^(.+) 已添加$/)
  if (added) return `${translateUiText(added[1], language)} added`
  const connected = text.match(/^(.+) 已连接$/)
  if (connected) return `${connected[1]} connected`
  const connecting = text.match(/^(.+) 正在连接$/)
  if (connecting) return `Connecting to ${connecting[1]}`
  const connection = text.match(/^连接：(.+)$/)
  if (connection) return `Connection: ${connection[1]}`
  const idle = text.match(/^无输出\s+(.+)$/)
  if (idle) return `Idle ${idle[1]}`
  const processes = text.match(/^(\d+)\s*进程$/)
  if (processes) return `${processes[1]} processes`
  return text
}

export function formatLocalizedDateTime(
  value: Date | number,
  language: ResolvedLanguage,
  timeZone: string,
  options: Intl.DateTimeFormatOptions,
) {
  const date = value instanceof Date ? value : new Date(value)
  try {
    return new Intl.DateTimeFormat(language, { ...options, timeZone }).format(date)
  } catch {
    return new Intl.DateTimeFormat(language, options).format(date)
  }
}
